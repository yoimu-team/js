/**
 * 翻譯
 * @param {string} text
 * @param {string} prefix
 * @returns {string}
 */
const t = (text, prefix) => {
	let result = ''
	for (let i = 0; i < text.length; i++) {
		const t = text[i]
		const st = stObj[`${prefix}${t}`] || t
		result += st
	}
	return result
}

// 繁轉簡
const toSimple = text => {
	return t(text, 't')
}

// 簡轉繁
const toTraditional = text => {
	return t(text, 's')
}

// 檢測中文字是否相等
const checkSameChinese = (text1, text2) => {
	if (text1.length !== text2.length) {
		return false
	}
	let same = true
	for (let i = 0; i < text1.length; i++) {
		const t1 = text1[i]
		const t2 = text2[i]
		const stt = stObj[`t${t1}`]
		if (stt != null) {
			if (!(stt === t2 || t1 === t2)) {
				same = false
				break
			}
		} else {
			if (t1 !== t2) {
				same = false
				break
			}
		}
	}
	return same
}

// 檢測文字(參數1)是否包含關鍵字(參數2)
const checkIncludeText = (text, keyword) => {
	return toSimple(text).includes(toSimple(keyword))
}

export { toSimple, toTraditional, checkSameChinese, checkIncludeText }
